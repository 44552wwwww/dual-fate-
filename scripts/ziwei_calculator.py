def an_daxian(sex, year_gan, wx_ju, ming_zhi):
    is_yang = GAN_YY[year_gan]==1
    shun = (is_yang and sex=="男") or (not is_yang and sex=="女")
    start = _gong_idx(ming_zhi); daxian = []
    for i in range(12):
        gi = (start+i)%12 if shun else (start-i)%12
        age = wx_ju*2+1 if i==0 else wx_ju*2+1+i*10
        daxian.append({"宫位":GONG_NAMES[gi],"地支":GONG_ZHI_ORDER[gi],"年龄段":f"{age}-{age+9}岁"})
    return {"排法":"顺行" if shun else "逆行","大限列表":daxian}

def get_miaoxian(star, zhi):
    return "庙旺" if (star in STAR_MIAO and zhi in STAR_MIAO[star]) else "利陷"

def compute_ziwei(year, month, day, hour, sex):
    birth = date(year, month, day)
    year_gan, year_zhi = [(TIAN_GAN[(year-4)%10], DI_ZHI[(year-4)%12])][0]
    ri_idx = ((birth-date(1900,1,1)).days+10)%60; ri_gan = TIAN_GAN[ri_idx%10]
    lunar_year, lunar_month, lunar_day = gregorian_to_lunar(year, month, day)
    shi_zhi = DI_ZHI[((hour+1)//2)%12]
    ming_gong, shen_gong = an_ming_shen_gong(lunar_month, shi_zhi)
    twelve = an_twelve_gongs(ming_gong)
    gong_gan = {gn:an_gong_gan(year_gan,gz) for gn,gz in twelve}
    wx_ju = an_wuxing_ju(gong_gan["命宫"], ming_gong)
    ziwei_zhi = an_ziwei(wx_ju, lunar_day)
    major = an_major_stars(ziwei_zhi)
    aux = an_aux_stars(year_zhi, lunar_month, lunar_day, shi_zhi, year_gan, ri_gan)
    sihua = an_sihua(year_gan)
    daxian = an_daxian(sex, year_gan, wx_ju, ming_gong)
    gongs_detail = []
    for gn, gz in twelve:
        gi = {"宫名":gn,"干支":gong_gan[gn]+gz,"天干":gong_gan[gn],"地支":gz,"主星":[],"辅星":[],"四化":[],"大限":None}
        for sn, sz in major.items():
            if sz==gz: gi["主星"].append({"星名":sn,"五行":STAR_WX.get(sn,""),"庙旺":get_miaoxian(sn,gz)})
        for sn, sz in aux.items():
            if sz==gz:
                tp = "吉" if sn in ["左辅","右弼","文昌","文曲","天魁","天钺","禄存"] else "煞" if sn in ["擎羊","陀罗","火星","铃星","地空","地劫"] else "杂"
                gi["辅星"].append({"星名":sn,"类型":tp})
        for hn, sn in sihua.items():
            if major.get(sn)==gz or aux.get(sn)==gz: gi["四化"].append({"化星":hn,"对应星":sn})
        for dx in daxian["大限列表"]:
            if dx["宫位"]==gn: gi["大限"]=dx["年龄段"]; break
        gongs_detail.append(gi)
    # 三方四正
    for i, g in enumerate(gongs_detail):
        g["三方四正"] = {"对宫":gongs_detail[(i+6)%12]["宫名"],"三合1":gongs_detail[(i+4)%12]["宫名"],"三合2":gongs_detail[(i+8)%12]["宫名"]}
    return {"输入":{"公历":f"{year}年{month}月{day}日 {hour}时","农历":f"{lunar_year}年{lunar_month}月{lunar_day}日","性别":sex,"时辰":shi_zhi+"时"},"年干支":year_gan+year_zhi,"日干":ri_gan,"命宫":ming_gong,"身宫":shen_gong,"五行局":{2:"水二局",3:"木三局",4:"金四局",5:"土五局",6:"火六局"}[wx_ju],"紫微星在":ziwei_zhi,"十二宫":gongs_detail,"四化":sihua,"大限":daxian}

