def gregorian_to_lunar(year, month, day):
    """公历转农历 - 返回 (农历年, 农历月, 农历日)"""
    birth = date(year, month, day)
    if year not in _SPRING_FESTIVAL:
        # Estimate: 春节约在1月21日-2月20日之间
        sf_m, sf_d = 2, 5
    else:
        sf_m, sf_d = _SPRING_FESTIVAL[year]
    sf = date(year, sf_m, sf_d)
    if birth < sf:
        if year-1 in _SPRING_FESTIVAL:
            prev_sf = date(year-1, *_SPRING_FESTIVAL[year-1])
        else:
            prev_sf = date(year-1, 2, 5)
        days_from_sf = (birth - prev_sf).days
        lunar_year = year - 1
    else:
        days_from_sf = (birth - sf).days
        lunar_year = year
    # 简化月日计算（月份大小月交替，大月30天，小月29天）
    lunar_month, remaining = 1, days_from_sf
    for m in range(1, 13):
        month_days = 30 if m % 2 == 1 else 29
        if remaining < month_days:
            lunar_month, lunar_day = m, remaining + 1
            break
        remaining -= month_days
    else:
        lunar_month, lunar_day = 12, remaining + 1
    return lunar_year, max(1, min(12, lunar_month)), max(1, min(30, lunar_day))

def _gong_idx(zhi): return GONG_ZHI_ORDER.index(zhi)

def an_ming_shen_gong(lunar_month, shi_zhi):
    mi = (_gong_idx("寅")+(lunar_month-1))%12
    ming_idx = (mi - DI_ZHI.index(shi_zhi))%12
    shen_idx = (mi + DI_ZHI.index(shi_zhi))%12
    return GONG_ZHI_ORDER[ming_idx], GONG_ZHI_ORDER[shen_idx]

def an_twelve_gongs(ming_zhi):
    start = _gong_idx(ming_zhi)
    return [(GONG_NAMES[i], GONG_ZHI_ORDER[(start-i)%12]) for i in range(12)]

